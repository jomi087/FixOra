
export interface GoogleTokenResponse {
  access_token: string;
  expires_in: number;
  refresh_token?: string;
  scope: string;
  token_type: string;
  id_token?: string;
}

export interface GoogleUserInfo {
  sub: string;
  name: string;
  given_name: string;
  family_name: string;
  picture: string;
  email: string;
  email_verified: boolean;
  locale: string;
}

export interface IGoogleOAuthService {
  exchangeCodeForToken(code: string): Promise<GoogleTokenResponse>;
  getUserInfo(accessToken: string): Promise<GoogleUserInfo>;
}